package org.monitor.models;

/**
 * Model to represent the instant status of the JVM
 * 
 * @author khalil.amdouni
 * 
 */
public class JVMInstantStatusModel {

	private String x;
	private double y;

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}
